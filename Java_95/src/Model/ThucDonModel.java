package Model;

public class ThucDonModel {
	private String monChin;
	private String monPhu;
	private double tongTien;
	public String getMonChin() {
		return monChin;
	}
	public void setMonChin(String monChin) {
		this.monChin = monChin;
	}
	public String getMonPhu() {
		return monPhu;
	}
	public void setMonPhu(String monPhu) {
		this.monPhu = monPhu;
	}
	public double getTongTien() {
		return tongTien;
	}
	public void setTongTien(double tongTien) {
		this.tongTien = tongTien;
	}

	

}
